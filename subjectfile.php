    <td>Web Programming</td>
    <td>This course offers students the basic skills of programming and website development. Students will learn to apply script writing skills on behalf of clients to create and create website sheets and write scripts on behalf of servers to create online database websites. For script writing on behalf of the client, students will learn HTML (HyperText Markup Language) and CSS (Cascading Style Sheet) to create structures and design styles for specific websites. Meanwhile, for script writing on behalf of the server, students will learn to create dynamic websites with databases using PHP (PHP: Hypertext Pre-processor) and MySQL (My Sequel). This course also requires students to build their own website using some of the latest website tools. The usability of the website will be emphasized throughout the learning process. During this semester, classes will be conducted using student-oriented methods and they need to be prepared before attending class activities. Different classroom activities and projects will be conducted to improve students' skills in website development. At the end of the course, students need to complete a project on website development that involves the application of techniques and knowledge gained in this course.</td>
  </tr>
  <tr>
    <td>Project Management</td>
    <td>This course covers the process of handling and managing a project, from the planning stage to the final stage. This course introduces students to the aspects of tasks and obstacles in managing a project, which is a very important function in dealing with a complex project. In the meantime, students are also exposed to methods on how to become a successful project manager and have competence in managing group members, scheduling, risk, and resources. This course also provides guidance to students in using project management equipment and behavioral competencies required in an organization, whether it is profitable or not. This course also emphasizes the factors of failure of a project and how it can be overcome through good planning.</td>
  </tr>
  <tr>
    <td>Cognitive Tools For Learning</td>
    <td>This course provides students with important experience to develop the knowledge, skills and attitudes required to use technology as a cognitive tool for an effective teaching medium that can improve power student thinking. Students will be introduced to theories of learning and practical considerations for the use of cognitive tools. The students will explore into the various applications of cognitive tools in learning and training fromangles of how they enable students to engage in, and help improve critical thinking and higher learning, and knowledge construction.</td>
  </tr>
  <tr>
    <td>Database Management System</td>
    <td>This course introduces students to the basics and importance of database management systems in software systems. Students will learn how to represent the data of an application using Entity-Relationship drawings and how to convert the representation into an appropriate relationship structure. Students will also learn to use SQL language to perform transaction operations with databases. Students will also practice how to write stand-alone software that interacts with the database system.</td>
  </tr>
  <tr>
    <td>Artificial Neural Networks</td>
    <td>This course provides exposure to Artificial Neural Networks (ANN) to students. ANN has been a subject of study of brain and neuronal network modeling since the 1940s. Several ANN models will be discussed in detail. This course contains modules comparison of biological and artificial neurons, discussion of general aspects of ANN, theory learning, and application of ANN in ​​business, medicine, and engineering technology. The course is also complemented by ANN training and study using appropriate software.</td>
  </tr>
